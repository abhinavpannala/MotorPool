package com.motorpool.dao;

public class RidesDAO {

	int rideid;
	int userid;
	long starttime;
	long endtime;
	String ridetype;
	String source;
	String destination;
	String stops;
	int ridepoints;
	int seats;
	String status;
	/**
	 * @return the rideid
	 */
	public int getRideid() {
		return rideid;
	}
	/**
	 * @param rideid the rideid to set
	 */
	public void setRideid(int rideid) {
		this.rideid = rideid;
	}
	/**
	 * @return the userid
	 */
	public int getUserid() {
		return userid;
	}
	/**
	 * @param userid the userid to set
	 */
	public void setUserid(int userid) {
		this.userid = userid;
	}
	/**
	 * @return the starttime
	 */
	public long getStarttime() {
		return starttime;
	}
	/**
	 * @param starttime the starttime to set
	 */
	public void setStarttime(long starttime) {
		this.starttime = starttime;
	}
	/**
	 * @return the endtime
	 */
	public long getEndtime() {
		return endtime;
	}
	/**
	 * @param endtime the endtime to set
	 */
	public void setEndtime(long endtime) {
		this.endtime = endtime;
	}
	/**
	 * @return the ridetype
	 */
	public String getRidetype() {
		return ridetype;
	}
	/**
	 * @param ridetype the ridetype to set
	 */
	public void setRidetype(String ridetype) {
		this.ridetype = ridetype;
	}
	/**
	 * @return the source
	 */
	public String getSource() {
		return source;
	}
	/**
	 * @param source the source to set
	 */
	public void setSource(String source) {
		this.source = source;
	}
	/**
	 * @return the destination
	 */
	public String getDestination() {
		return destination;
	}
	/**
	 * @param destination the destination to set
	 */
	public void setDestination(String destination) {
		this.destination = destination;
	}
	/**
	 * @return the stops
	 */
	public String getStops() {
		return stops;
	}
	/**
	 * @param stops the stops to set
	 */
	public void setStops(String stops) {
		this.stops = stops;
	}
	/**
	 * @return the ridepoints
	 */
	public int getRidepoints() {
		return ridepoints;
	}
	/**
	 * @param ridepoints the ridepoints to set
	 */
	public void setRidepoints(int ridepoints) {
		this.ridepoints = ridepoints;
	}
	/**
	 * @return the seats
	 */
	public int getSeats() {
		return seats;
	}
	/**
	 * @param seats the seats to set
	 */
	public void setSeats(int seats) {
		this.seats = seats;
	}
	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
