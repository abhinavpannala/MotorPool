package com.motorpool.dao;

public class AdminDAO {

	int adminpoints;
	float percentage;
	/**
	 * @return the adminpoints
	 */
	public int getAdminpoints() {
		return adminpoints;
	}
	/**
	 * @param adminpoints the adminpoints to set
	 */
	public void setAdminpoints(int adminpoints) {
		this.adminpoints = adminpoints;
	}
	/**
	 * @return the percentage
	 */
	public float getPercentage() {
		return percentage;
	}
	/**
	 * @param percentage the percentage to set
	 */
	public void setPercentage(float percentage) {
		this.percentage = percentage;
	}
	
	
}
